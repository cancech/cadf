// Helper macros to determine where to start processing the iterative generation macros based on the size of the input
#define macro_dispatcher(func, ...) \
            macro_dispatcher_(func, VA_NUM_ARGS(__VA_ARGS__))
#define macro_dispatcher_(func, nargs) \
            macro_dispatcher__(func, nargs)
#define macro_dispatcher__(func, nargs) \
            func##nargs




// Add dependency configurations
#define DEPENDENCIES(...) \
	static void registerDependentConfigurations(cadf::di::Context* context) { \
		static bool isLoading = false; \
		if (isLoading) return; \
		isLoading = true; \
		context->registerConfiguration<__VA_ARGS__>(); \
		isLoading = false; \
	}




